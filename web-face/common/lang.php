function getLM($str) {
	if ($GLOBALS["_msgArr"][$str][$GLOBALS["_langId"]]) {
		return $GLOBALS["_msgArr"][$str][$GLOBALS["_langId"]];
	}
	else {
		foreach ($GLOBALS["_msgArr"] as $key => $val) {
			if (strtolower($key) == strtolower($str)) {

				if ($str == strtolower($str)) return strtolower($val[$GLOBALS["_langId"]]);
				if ($str == strtoupper($str)) return strtoupper($val[$GLOBALS["_langId"]]);
				if ($str == ucfirst($str)) return ucfirst($val[$GLOBALS["_langId"]]);
				if ($str == ucwords($str)) return ucwords($val[$GLOBALS["_langId"]]);
				return $val[$GLOBALS["_langId"]];
			}
		}
	}
	return $str;
}

